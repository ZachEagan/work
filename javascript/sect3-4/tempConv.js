let fahrenheit = 28 // 0c or 273.15k

//calculate celsius and store in celsius variable
// print value (F − 32) × 5/9
let celsius = (fahrenheit - 32) * (5/9)
console.log("Celsius is " + celsius)

//calculate kelvin value and store in variable print in variable
// (fahrenheit − 32) × 5/9 + 273.15 
let kelvin = (fahrenheit - 32) * (5/9) + 273.15 
console.log("Kelvin is " + kelvin)